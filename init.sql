create table if not exists homicide_info (
    id numeric,
    first_name text,
    last_name text,
    age numeric,
    gender text,
    race text,
    cause text,
    death_loc text,
    district text,
    street_address text,
    zip_code text,
    latitude numeric,
    longitude numeric,
    date date,
    time time,
    notes text
);